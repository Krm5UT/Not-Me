using System.Collections;
using UnityEngine;

/// <summary>
/// VRObjectLoop Ś Spawn an object (car, UFO, whatever), move it past the player,
/// then reset and loop when the player enters a trigger zone.
///
/// SETUP (5 steps, no prefab chaos):
///  1. Create an empty GameObject Ś call it "VRObjectLoop Manager".
///  2. Attach this script to it.
///  3. Assign your car/object prefab to [objectPrefab].
///  4. Place two empty GameObjects in your scene:
///       Ģ spawnPoint  Ś where the object appears (e.g., far end of road)
///       Ģ despawnPoint Ś where it disappears (e.g., behind the player)
///  5. Create a Box Collider on any GameObject, tick "Is Trigger", and assign
///     it to [loopTrigger]. This is what the player walks into to restart the loop.
///
/// That's it. Hit Play.
/// </summary>
public class VRObjectLoop : MonoBehaviour
{
    [Header("Object Settings")]
    [Tooltip("The car, drone, spaceship Ś whatever you want to loop.")]
    public GameObject objectPrefab;

    [Tooltip("Where the object spawns / resets to.")]
    public Transform spawnPoint;

    [Tooltip("Once the object passes this point it gets recycled.")]
    public Transform despawnPoint;

    [Header("Movement")]
    [Tooltip("Speed in units/second along the direction spawnPoint ? despawnPoint.")]
    public float moveSpeed = 8f;

    [Tooltip("Extra delay (seconds) before the object appears after a reset.")]
    public float spawnDelay = 0.5f;

    [Header("Trigger")]
    [Tooltip("The trigger collider the player walks into to restart the sequence.")]
    public Collider loopTrigger;

    [Tooltip("Tag used to identify the VR player / camera rig.")]
    public string playerTag = "Player";

    // ?? Private state ??????????????????????????????????????????????????????????
    private GameObject _activeObject;   // the live instance
    private bool       _moving;         // is it currently travelling?
    private bool       _waitingForLoop; // did we already queue a restart?

    // ??????????????????????????????????????????????????????????????????????????
    void Start()
    {
        ValidateSetup();
        SpawnObject(); // kick things off immediately
    }

    // ??????????????????????????????????????????????????????????????????????????
    void Update()
    {
        if (!_moving || _activeObject == null) return;

        // Move the object every frame
        _activeObject.transform.position = Vector3.MoveTowards(
            _activeObject.transform.position,
            despawnPoint.position,
            moveSpeed * Time.deltaTime
        );

        // Reached the despawn point?
        float distanceToDespawn = Vector3.Distance(
            _activeObject.transform.position,
            despawnPoint.position
        );

        if (distanceToDespawn < 0.1f)
        {
            HideObject();
            // Object will reappear only when player enters the trigger
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Called by the trigger child object (see TriggerRelay below)
    public void OnPlayerEnteredTrigger()
    {
        if (_waitingForLoop) return; // prevent double-firing
        _waitingForLoop = true;
        StartCoroutine(ResetAndSpawn());
    }

    // ??????????????????????????????????????????????????????????????????????????
    IEnumerator ResetAndSpawn()
    {
        yield return new WaitForSeconds(spawnDelay);
        SpawnObject();
        _waitingForLoop = false;
    }

    // ??????????????????????????????????????????????????????????????????????????
    void SpawnObject()
    {
        if (_activeObject == null)
        {
            // First time Ś instantiate
            _activeObject = Instantiate(objectPrefab, spawnPoint.position, spawnPoint.rotation);
        }
        else
        {
            // Recycle Ś just teleport and re-enable
            _activeObject.transform.position = spawnPoint.position;
            _activeObject.transform.rotation = spawnPoint.rotation;
            _activeObject.SetActive(true);
        }

        // Face the despawn direction (optional but looks right for cars)
        Vector3 dir = (despawnPoint.position - spawnPoint.position).normalized;
        if (dir != Vector3.zero)
            _activeObject.transform.rotation = Quaternion.LookRotation(dir);

        _moving = true;
    }

    // ??????????????????????????????????????????????????????????????????????????
    void HideObject()
    {
        _moving = false;
        if (_activeObject != null)
            _activeObject.SetActive(false);
    }

    // ??????????????????????????????????????????????????????????????????????????
    void ValidateSetup()
    {
        if (objectPrefab  == null) Debug.LogError("[VRObjectLoop] objectPrefab is not assigned!");
        if (spawnPoint    == null) Debug.LogError("[VRObjectLoop] spawnPoint is not assigned!");
        if (despawnPoint  == null) Debug.LogError("[VRObjectLoop] despawnPoint is not assigned!");
        if (loopTrigger   == null) Debug.LogError("[VRObjectLoop] loopTrigger is not assigned!");

        // Auto-attach relay if the trigger collider is missing it
        if (loopTrigger != null)
        {
            TriggerRelay relay = loopTrigger.GetComponent<TriggerRelay>();
            if (relay == null) relay = loopTrigger.gameObject.AddComponent<TriggerRelay>();
            relay.owner = this;
            relay.playerTag = playerTag;
        }
    }
}

// ???????????????????????????????????????????????????????????????????????????????
/// <summary>
/// Lightweight relay that lives on the trigger collider and pings VRObjectLoop.
/// You don't need to edit this Ś it's auto-attached at runtime.
/// </summary>
public class TriggerRelay : MonoBehaviour
{
    [HideInInspector] public VRObjectLoop owner;
    [HideInInspector] public string playerTag = "Player";

    void OnTriggerEnter(Collider other)
    {
        if (other.CompareTag(playerTag))
            owner.OnPlayerEnteredTrigger();
    }
}